package dayTwo.Expression;

public class AutoIncrement {
	
	
	//++ and --
	
	
	public static void main(String[] args) {
		
		int num1 = 100;
		int num2 = 200;
		
		num1++;
		System.out.println(num1);
		
		num2--;
		System.out.println(num2);

		++num1;
		System.out.println(num1);
		
		--num2;
		System.out.println(num2);
	}

}
