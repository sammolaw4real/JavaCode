package dayTwo.Expression;

public class ComparisonOperators {

	// We have six relational operator in Java: ==, !=, >, <, >=, <=

	public static void main(String[] args) {

		int num1 = 10;
		int num2 = 50;

		if (num1 == num2) {

			System.out.println("Num 1 and Num 2 are the same");
		}

		else {

			System.out.println("Num 1 and Num 2 are not the same");
		}

		if (num1 != num2) {

			System.out.println("Num 1 and Num 2 are the same");
		}

		else {

			System.out.println("Num 1 and Num 2 are not the same");
		}

		if (num1 > num2) {

			System.out.println("Num 1 and Num 2 are the same");
		}

		else {

			System.out.println("Num 1 and Num 2 are the the same");
		}
		
		if(num1 < num2) {
			
			System.out.println("Num 1 and Num 2 are the same");
		} 
		
		else {
			
			System.out.println("Num 1 and Num 2 are not the same");
		}
		
		if(num1 >= num2) {
			
			System.out.println("Num 1 and Num 2 are the same");
		} 
		
		else {
			
			System.out.println("Num 1 and Num 2 are not the same");
		}
		
		if(num1 <= num2) {
			
			System.out.println("Num 1 and Num 2 are the same");
		} 
		
		else {
			
			System.out.println("Num 1 and Num 2 are not the same");
		}
		
		
	}

}
