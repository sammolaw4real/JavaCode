package dayTwo.Expression;


public class LogicalOperators {

	
	//Logical operators in java are: &&,||, !
	
	
	public static void main(String[] args) {
		
		boolean b1 = true;
		boolean b2 = false;
		
		System.out.println(b1 && b2);
		System.out.println(b1 || b2);
		System.out.println(!(b1 && b2));

	}

}
